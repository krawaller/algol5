import { executeOrder } from "../../../executors";
import { emptyFullDef } from "../../../../../common";
import { AlgolOrderAnon, AlgolStatementSuite } from "../../../../../types";

export const testSuite: AlgolStatementSuite<AlgolOrderAnon> = {
  title: "Artifacts - Walker - Count",
  func: executeOrder,
  defs: [
    {
      def: {
        ...emptyFullDef,
        boards: {
          basic: {
            ...emptyFullDef.boards.basic,
            height: 4,
            width: 4,
          },
        },
        generators: {
          totalCount: {
            type: "walker",
            dir: 1,
            start: "marka1",
            count: { singles: ["marka2", "marka4"] },
            draw: {
              start: {
                tolayer: "flarps",
                include: { total: ["totalcount"] },
              },
            },
          },
          drawCount: {
            type: "walker",
            dir: 1,
            start: "marka1",
            count: { singles: ["marka2", "marka4"] },
            draw: {
              counted: {
                tolayer: "flarps",
                include: { total: ["totalcount"] },
              },
            },
          },
          totalAndLast: {
            type: "walker",
            dir: 1,
            start: "marka1",
            count: { singles: ["marka2", "marka4"] },
            draw: {
              last: { tolayer: "flarps", include: { n: ["totalcount"] } },
            },
          },
          countSoFar: {
            type: "walker",
            dir: 1,
            start: "marka1",
            startasstep: true,
            count: { singles: ["marka2", "marka4"] },
            draw: {
              steps: {
                tolayer: "flarps",
                include: { now: ["countsofar"] },
              },
            },
          },
        },
      },
      player: 1,
      action: "someaction",
      contexts: [
        {
          context: {
            MARKS: {
              marka1: "a1",
              marka2: "a2",
              marka3: "a3",
              marka4: "a4",
              markb1: "b1",
              markd4: "d4",
            },
            ARTIFACTS: { flarps: {} },
          },
          tests: [
            {
              expr: { generators: ["totalCount"] },
              asserts: [
                {
                  sample: "ARTIFACTS.flarps",
                  res: { a1: { total: 2 } },
                  desc: "It can count a given set",
                },
              ],
            },
            {
              expr: { generators: ["drawCount"] },
              asserts: [
                {
                  sample: "ARTIFACTS.flarps",
                  res: { a2: { total: 2 }, a4: { total: 2 } },
                  desc: "It can draw the counted steps",
                },
              ],
            },
            {
              expr: { generators: ["totalAndLast"] },
              asserts: [
                {
                  sample: "ARTIFACTS.flarps",
                  res: { a4: { n: 2 } },
                  desc: "It can handle last and totalcount",
                },
              ],
            },
            {
              expr: { generators: ["countSoFar"] },
              asserts: [
                {
                  sample: "ARTIFACTS.flarps",
                  res: {
                    a1: { now: 0 },
                    a2: { now: 1 },
                    a3: { now: 1 },
                    a4: { now: 2 },
                  },
                  desc: "It can tell you current count",
                },
              ],
            },
          ],
        },
      ],
    },
  ],
};
